// Helper function to get default permissions based on role
function getDefaultPermissions(role) {
  const permissions = {
    admin: {
      dashboard: true,
      users: true,
      products: true,
      orders: true,
      payments: true,
      settings: true,
      marketing: true
    },
    manager: {
      dashboard: true,
      users: false,
      products: true,
      orders: true,
      payments: true,
      settings: false,
      marketing: true
    },
    support: {
      dashboard: true,
      users: false,
      products: false,
      orders: true,
      payments: false,
      settings: false,
      marketing: false
    }
  };

  return permissions[role] || permissions.support;
}

